def baixar_arquivo_sih_bruto(uf: str, ano: int, mes: int) -> str:
    pasta_ano = DATA_RAW / str(ano)
    pasta_ano.mkdir(parents=True, exist_ok=True)
    nome = f'sih_rd_{uf}_{ano}_{mes:02d}.parquet'
    caminho = pasta_ano / nome

    if caminho.exists() and arquivo_valido(caminho):
        return 'ja_valido'
    elif caminho.exists():
        caminho.unlink()
        status_base = 'redownload'
    else:
        status_base = 'baixado'

    try:
        df = baixar_e_ler_sih(uf, ano, mes, DATA_RAW / '_tmp')
        df.to_parquet(caminho, index=False, compression='snappy')
        del df
        gc.collect()
        return status_base
    except FileNotFoundError:
        return 'nao_encontrado'
    except Exception as e:
        log.error(f'Erro {uf}/{ano}-{mes:02d}: {type(e).__name__}: {e}')
        return f'erro:{type(e).__name__}'


def executar_download_massa() -> dict:
    contadores = {'ja_valido':0,'baixado':0,'redownload':0,
                  'nao_encontrado':0,'erro':0}
    total = len(ANOS_TODOS) * len(MESES) * len(UFS_BR)
    processados = 0
    inicio = time.time()
    log.info(f'Iniciando download: {total} arquivos')

    for ano in ANOS_TODOS:
        for mes in MESES:
            for uf in UFS_BR:
                processados += 1
                status = baixar_arquivo_sih_bruto(uf, ano, mes)
                chave = status if ':' not in status else 'erro'
                contadores[chave] = contadores.get(chave, 0) + 1

                if processados % 50 == 0 or 'erro' in status:
                    elapsed = time.time() - inicio
                    eta = str(timedelta(seconds=int(
                        (total - processados) / max(processados/elapsed, 0.01)
                    )))
                    log.info(
                        f'[{processados}/{total}] '
                        f'✅{contadores["baixado"]} '
                        f'⏭️{contadores["ja_valido"]} '
                        f'❌{contadores["erro"]} '
                        f'| ETA: {eta} | {uf}/{ano}-{mes:02d} → {status}'
                    )

    log.info('DOWNLOAD CONCLUÍDO')
    log.info(f'Baixados: {contadores["baixado"]} | '
             f'Já existiam: {contadores["ja_valido"]} | '
             f'Erros: {contadores["erro"]}')
    return contadores


