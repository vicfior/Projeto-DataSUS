def processar_arquivo_drsai(uf: str, ano: int, mes: int) -> str:
    pasta_interim_ano = DATA_INTERIM / str(ano)
    pasta_interim_ano.mkdir(parents=True, exist_ok=True)
    caminho_interim = pasta_interim_ano / f'sih_drsai_{uf}_{ano}_{mes:02d}.parquet'
    caminho_bruto   = DATA_RAW / str(ano) / f'sih_rd_{uf}_{ano}_{mes:02d}.parquet'

    if caminho_interim.exists() and arquivo_valido(caminho_interim):
        return 'ja_processado'
    if not caminho_bruto.exists():
        return 'bruto_ausente'

    try:
        df = pq.read_table(caminho_bruto, columns=COLUNAS_SIH).to_pandas()
        df = normalizar_tipos_sih(df)
        df['categoria_drsai'] = df['DIAG_PRINC'].apply(categorizar_drsai)
        df_drsai = df[df['categoria_drsai'] != 'fora_escopo'].copy()
        df_drsai.to_parquet(caminho_interim, index=False, compression='snappy')
        status = 'sem_drsai' if len(df_drsai) == 0 else 'processado'
        del df, df_drsai
        gc.collect()
        return status
    except Exception as e:
        log.error(f'Erro filtro {uf}/{ano}-{mes:02d}: {type(e).__name__}: {e}')
        return f'erro:{type(e).__name__}'


def executar_filtro_drsai() -> dict:
    contadores = {'ja_processado':0,'processado':0,
                  'sem_drsai':0,'bruto_ausente':0,'erro':0}
    total = len(ANOS_TODOS) * len(MESES) * len(UFS_BR)
    processados = 0
    inicio = time.time()

    log.info(f'Iniciando filtro DRSAI: {total} arquivos')

    for ano in ANOS_TODOS:
        for mes in MESES:
            for uf in UFS_BR:
                processados += 1
                status = processar_arquivo_drsai(uf, ano, mes)
                chave = status if ':' not in status else 'erro'
                contadores[chave] = contadores.get(chave, 0) + 1

                if processados % 100 == 0 or 'erro' in status:
                    elapsed = time.time() - inicio
                    eta = str(timedelta(seconds=int(
                        (total - processados) / max(processados/elapsed, 0.01)
                    )))
                    log.info(
                        f'[{processados}/{total}] '
                        f'✅{contadores["processado"]} '
                        f'⏭️{contadores["ja_processado"]} '
                        f'❌{contadores["erro"]} '
                        f'| ETA: {eta} | {uf}/{ano}-{mes:02d} → {status}'
                    )

    log.info('=' * 60)
    log.info('FILTRO CONCLUÍDO')
    log.info(f'Processados:    {contadores["processado"]:>5}')
    log.info(f'Já existiam:    {contadores["ja_processado"]:>5}')
    log.info(f'Sem DRSAI:      {contadores["sem_drsai"]:>5}')
    log.info(f'Brutos ausentes:{contadores["bruto_ausente"]:>5}')
    log.info(f'Erros:          {contadores["erro"]:>5}')
    return contadores

print('✅ Função de filtro pronta. Execute:')
