# Classifica risco SOMENTE entre os municípios com pelo menos 1 óbito
# usando os percentis da distribuição dos positivos no baseline

df_baseline_pos = df_baseline[df_baseline['n_obitos_drsai'] > 0]
p66 = df_baseline_pos['n_obitos_drsai'].quantile(0.66)
p90 = df_baseline_pos['n_obitos_drsai'].quantile(0.90)

print(f"Limiar Médio→Alto (p66 dos positivos): {p66:.0f}")
print(f"Limiar Alto       (p90 dos positivos): {p90:.0f}")

def classificar(x):
    if x == 0:
        return 0          # Baixo — sem evento
    elif x <= p66:
        return 1          # Médio — evento leve
    else:
        return 2          # Alto  — evento severo

df['risco_ternario'] = df['n_obitos_drsai'].apply(classificar)
print(df['risco_ternario'].value_counts(normalize=True).sort_index())

