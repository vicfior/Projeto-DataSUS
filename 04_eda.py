df_baseline = df[df['ANO_CMPT'].isin([2017, 2018, 2019])]
mu    = df_baseline['n_obitos_drsai'].mean()
sigma = df_baseline['n_obitos_drsai'].std()

print(f'Baseline 2017–2019: µ = {mu:.4f} | σ = {sigma:.4f}')
print()
print(f'{"k1":>6} {"k2":>6} | {"Limiar Médio":>14} {"Limiar Alto":>12} | {"% Baixo":>8} {"% Médio":>8} {"% Alto":>8}')
print('-' * 78)

resultados_thresh = []
for k1 in [0.1, 0.25, 0.5, 0.75]:
    for k2 in [0.5, 0.75, 1.0, 1.5]:
        if k2 <= k1:
            continue
        lm = mu + k1 * sigma
        la = mu + k2 * sigma
        labels = df['n_obitos_drsai'].apply(
            lambda x: 2 if x >= la else (1 if x >= lm else 0)
        )
        dist    = labels.value_counts(normalize=True)
        p_baixo = dist.get(0, 0)
        p_medio = dist.get(1, 0)
        p_alto  = dist.get(2, 0)
        resultados_thresh.append((k1, k2, lm, la, p_baixo, p_medio, p_alto))
        flag = '  <- CANDIDATO' if p_medio >= 0.03 and p_alto >= 0.02 else ''
        print(f'{k1:>6.2f} {k2:>6.2f} | {lm:>14.4f} {la:>12.4f} | {p_baixo:>7.1%} {p_medio:>8.1%} {p_alto:>8.1%}{flag}')

# Visualiza distribuição de classes para cada par candidato
candidatos = [(r[0], r[1]) for r in resultados_thresh if r[5] >= 0.03 and r[6] >= 0.02]

if candidatos:
    n_cand = min(len(candidatos), 4)
    fig, axes = plt.subplots(1, n_cand, figsize=(4 * n_cand, 4))
    if n_cand == 1:
        axes = [axes]

    cores_bar = ['#3498DB', '#F39C12', '#E74C3C']
    for ax, (k1, k2) in zip(axes, candidatos[:4]):
        lm = mu + k1 * sigma
        la = mu + k2 * sigma
        labels = df['n_obitos_drsai'].apply(
            lambda x: 2 if x >= la else (1 if x >= lm else 0)
        )
        counts = labels.value_counts().sort_index()
        bars = ax.bar(['Baixo', 'Médio', 'Alto'],
                      [counts.get(0,0), counts.get(1,0), counts.get(2,0)],
                      color=cores_bar)
        ax.set_title(f'k1={k1}, k2={k2}\nLm={lm:.3f} | La={la:.3f}',
                     fontsize=9, fontweight='bold')
        ax.set_ylabel('Frequência')
        for bar in bars:
            h = bar.get_height()
            ax.text(bar.get_x() + bar.get_width()/2., h,
                    f'{h:,.0f}\n({100*h/len(df):.1f}%)',
                    ha='center', va='bottom', fontsize=8)

    fig.suptitle('Distribuição de Classes por Par (k1, k2) — Threshold µ + k*σ', fontweight='bold')
    plt.tight_layout()
    plt.savefig('/content/drive/MyDrive/projeto_drsai/eda_02_calibracao_threshold.png',
                dpi=150, bbox_inches='tight')
    plt.show()
    print('Salvo: eda_02_calibracao_threshold.png')
