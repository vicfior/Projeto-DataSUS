FEATURES_V2 = [f for f in FEATURES_V2 if f in df.columns]

df_modelo_v2 = df[FEATURES_V2 + ['houve_obito', 'ANO_CMPT']].dropna()

X_train_v2 = df_modelo_v2[df_modelo_v2['ANO_CMPT'].isin([2017,2018,2019])][FEATURES_V2]
y_train_v2 = df_modelo_v2[df_modelo_v2['ANO_CMPT'].isin([2017,2018,2019])]['houve_obito']
