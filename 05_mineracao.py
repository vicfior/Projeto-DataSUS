print(f'AUC-ROC  : {roc_auc_score(y_test_v2, y_proba_v2):.4f}')

coef_v2 = pd.DataFrame({
    'feature':     FEATURES_V2,
    'coeficiente': model_v2.coef_[0],
    'odds_ratio':  np.exp(model_v2.coef_[0])
}).sort_values('odds_ratio', ascending=False)

print('COEFICIENTES V2:')
print()
