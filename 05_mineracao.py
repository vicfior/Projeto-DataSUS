from google.colab import drive
drive.mount('/content/drive')

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.ticker as mticker
import seaborn as sns
import warnings

from sklearn.linear_model import LogisticRegression
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import (
    classification_report,
    confusion_matrix,
    ConfusionMatrixDisplay,
    roc_auc_score,
    RocCurveDisplay,
    f1_score,
)

warnings.filterwarnings('ignore')
sns.set_theme(style='whitegrid', font_scale=1.1)
plt.rcParams['figure.dpi'] = 130

PATH_PARQUET  = '/content/drive/MyDrive/projeto_drsai/data/processed/analitico_municipio_mes.parquet'
PATH_OUTPUTS  = '/content/drive/MyDrive/projeto_drsai/'

df = pd.read_parquet(PATH_PARQUET)
print(f'✅ Dataset carregado: {df.shape[0]:,} linhas × {df.shape[1]} colunas')
print(f'   Anos disponíveis: {sorted(df["ANO_CMPT"].unique())}')

# ── TARGET BINÁRIO ────────────────────────────────────────────────────────────
df['houve_obito'] = (df['n_obitos_drsai'] >= 1).astype(int)

print('Distribuição do target (dataset completo):')
print(df['houve_obito'].value_counts(normalize=True)
      .rename({0: 'Sem óbito (0)', 1: 'Com óbito (1)'}))

# ── FEATURES ─────────────────────────────────────────────────────────────────
FEATURES = [f for f in [
    'n_internacoes_drsai',
    'fecal_oral', 'leptospirose', 'helmintiase', 'arboviral', 'malaria',
    'idade_media', 'permanencia_media',
    'indice_atendimento_total_agua',
    'indice_coleta_esgoto',
    'indice_tratamento_esgoto',
    'indice_perda_distribuicao_agua',
    'indice_esgotamento_agua_consumida',
    'MES_CMPT',
] if f in df.columns]

print(f'\nFeatures selecionadas ({len(FEATURES)}): {FEATURES}')

# ── DATASET DE MODELAGEM (remove nulos) ───────────────────────────────────────
df_modelo = df[FEATURES + ['houve_obito', 'ANO_CMPT']].dropna()
print(f'\nShape após remover nulos: {df_modelo.shape[0]:,} linhas')

# ── SPLIT TEMPORAL ────────────────────────────────────────────────────────────
# Treina com padrão histórico pré-pandemia, avalia em dados recentes
# Isso simula o uso real: treinar no passado para prever o futuro

df_treino = df_modelo[df_modelo['ANO_CMPT'].isin([2017, 2018, 2019])]
df_teste  = df_modelo[df_modelo['ANO_CMPT'].isin([2023, 2024])]

X_train = df_treino[FEATURES]
y_train = df_treino['houve_obito']

X_test  = df_teste[FEATURES]
y_test  = df_teste['houve_obito']

print('Split temporal:')
print(f'  Treino (2017–2019): {len(X_train):>7,} linhas')
print(f'  Teste  (2023–2024): {len(X_test):>7,} linhas')
print()
print('Distribuição do target no treino:')
print(y_train.value_counts(normalize=True).rename({0:'Sem óbito', 1:'Com óbito'}))
print()
print('Distribuição do target no teste:')
print(y_test.value_counts(normalize=True).rename({0:'Sem óbito', 1:'Com óbito'}))

# ESCALONAMENTO
# µ — Média (soma dos valores / qtd de observações)
# σ — Desvio padrão (mede o quanto os valores se afastam da média
# (grau de dispersão))

scaler = StandardScaler()
X_train_sc = scaler.fit_transform(X_train)   # aprende µ e σ do treino
X_test_sc  = scaler.transform(X_test)        # aplica os mesmos µ e σ

print('Escalonamento aplicado:')
for i, feat in enumerate(FEATURES):
    print(f'  {feat:<42}  µ={scaler.mean_[i]:>8.3f}  σ={scaler.scale_[i]:>8.3f}')

# TREINAMENTO
# class_weight='balanced': penaliza proporcionalmente os erros na classe
# minoritária (Com óbito = 6.5%), evitando que o modelo preveja sempre 0
# solver='lbfgs': recomendado para datasets médios com regularização L2

model = LogisticRegression(
    class_weight='balanced',
    solver='lbfgs',
    max_iter=1000,
    random_state=42
)

model.fit(X_train_sc, y_train)
print('Modelo treinado.')
print(f'Iterações até convergência: {model.n_iter_[0]}')

"""### Conceitos
- Support: quantas amostras reais existem de cada classe no conjunto de teste.
```
Sem Óbito (0) → support = 64.482  (municípios que realmente não tiveram óbito)
Com Óbito (1) → support =  5.850  (municípios que realmente tiveram óbito)
Total          →           70.33
```
- Precision: de tudo que o modelo previu como positivo, o que REALMENTE é positivo? (se é falso positivo ou não)\
Precision = VP / (VP + FP)
```
Com Óbito (1) → Precision = 0.16

Significa: de cada 100 municípios que o modelo classificou como
"com óbito", apenas 16 realmente tiveram óbito.
Os outros 84 foram falsos alarmes.
```
- Recall: de todos os positivos reais, quantos o modelo conseguiu detectar de verdade\
Recall = VP / (VP + FN)
```
Com Óbito (1) → Recall = 0.75

Significa: de cada 100 municípios que realmente tiveram óbito,
o modelo detectou 75.
Os outros 25 passaram despercebidos (falsos negativos).
```
- F1-score: média harmônica entre recall e precision.\
F1 = 2 * [(Precision * Recall) / (Precision + Recall)]
```
Com Óbito (1) → F1 = 2 × (0.16 × 0.75) / (0.16 + 0.75) = 0.27

O F1 baixo (0.27) revela que o modelo está desequilibrado:
recall alto (75%) mas precision muito baixa (16%).
```


"""

# PREDIÇÃO
y_pred  = model.predict(X_test_sc)
y_proba = model.predict_proba(X_test_sc)[:, 1] # P(houve_obito=1)

# MÉTRICAS
print('=' * 55)
print('AVALIAÇÃO DO MODELO — CONJUNTO DE TESTE (2023–2024)')
print('=' * 55)

print('\nRelatório de Classificação:')
print(classification_report(
    y_test, y_pred,
    target_names=['Sem Óbito (0)', 'Com Óbito (1)']
))

macro_f1 = f1_score(y_test, y_pred, average='macro')
auc      = roc_auc_score(y_test, y_proba)

print(f'Macro F1-Score : {macro_f1:.4f}  ← métrica principal')
print(f'AUC-ROC        : {auc:.4f}')

# VISUALIZAÇÕES
fig, axes = plt.subplots(1, 2, figsize=(14, 5))

# Matriz de confusão
cm = confusion_matrix(y_test, y_pred)
disp = ConfusionMatrixDisplay(cm, display_labels=['Sem Óbito', 'Com Óbito'])
disp.plot(ax=axes[0], cmap='Blues', colorbar=False)
axes[0].set_title('Matriz de Confusão\n(Teste 2023–2024)', fontweight='bold')

# Curva ROC
RocCurveDisplay.from_predictions(
    y_test, y_proba, ax=axes[1],
    name=f'Regressão Logística (AUC={auc:.3f})',
    color='#2471A3'
)
axes[1].plot([0,1],[0,1], 'k--', linewidth=1, label='Aleatório (AUC=0.500)')
axes[1].set_title('Curva ROC\n(Teste 2023–2024)', fontweight='bold')
axes[1].legend(fontsize=9)

plt.tight_layout()
plt.savefig(PATH_OUTPUTS + 'mineracao_01_confusao_roc.png', dpi=150, bbox_inches='tight')
plt.show()
print('Salvo: mineracao_01_confusao_roc.png')

# COEFICIENTES E ODDS RATIO
# Odds Ratio = e^coef
# OR > 1: aumenta a chance de óbito
# OR < 1: reduz a chance de óbito
# OR = 1: sem efeito

coef_df = pd.DataFrame({
    'feature':     FEATURES,
    'coeficiente': model.coef_[0],
    'odds_ratio':  np.exp(model.coef_[0])
}).sort_values('odds_ratio', ascending=False)

print('COEFICIENTES DO MODELO (ordenado por Odds Ratio):')
print()
print(f'{"Feature":<42} {"Coef":>8} {"Odds Ratio":>12}  Direção')
print('-' * 75)
for _, row in coef_df.iterrows():
    direcao = '▲ aumenta risco' if row['odds_ratio'] > 1 else '▼ reduz risco'
    print(f'{row["feature"]:<42} {row["coeficiente"]:>8.4f} {row["odds_ratio"]:>12.4f}  {direcao}')

print()
print('Intercepto:', round(model.intercept_[0], 4))

fig, axes = plt.subplots(1, 2, figsize=(16, 6))

cores = ['#E74C3C' if v > 1 else '#2ECC71' for v in coef_df['odds_ratio']]

# Painel 1 — Odds Ratio
axes[0].barh(coef_df['feature'], coef_df['odds_ratio'], color=cores)
axes[0].axvline(1.0, color='black', linestyle='--', linewidth=1.2, label='OR = 1 (sem efeito)')
axes[0].set_xlabel('Odds Ratio')
axes[0].set_title('Odds Ratio por Feature\n(vermelho=aumenta risco | verde=reduz risco)',
                  fontweight='bold')
axes[0].legend(fontsize=8)

# Painel 2 — Coeficientes brutos
cores_coef = ['#E74C3C' if v > 0 else '#2ECC71' for v in coef_df['coeficiente']]
axes[1].barh(coef_df['feature'], coef_df['coeficiente'], color=cores_coef)
axes[1].axvline(0, color='black', linestyle='--', linewidth=1.2)
axes[1].set_xlabel('Coeficiente')
axes[1].set_title('Coeficientes Brutos\n(positivo=aumenta risco | negativo=reduz risco)',
                  fontweight='bold')

plt.tight_layout()
plt.savefig(PATH_OUTPUTS + 'mineracao_02_coeficientes.png', dpi=150, bbox_inches='tight')
plt.show()
print('Salvo: mineracao_02_coeficientes.png')

# RESUMO
print('=' * 60)
print('RESUMO — REGRESSÃO LOGÍSTICA BINÁRIA | DRSAI')
print('=' * 60)

tn, fp, fn, tp = cm.ravel()
