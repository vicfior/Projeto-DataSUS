        idade_media                       = ('idade_media',                       'mean'),
        permanencia_media                 = ('permanencia_media',                 'mean'),
        indice_atendimento_total_agua     = ('indice_atendimento_total_agua',     'mean'),
        indice_coleta_esgoto              = ('indice_coleta_esgoto',              'mean'),
